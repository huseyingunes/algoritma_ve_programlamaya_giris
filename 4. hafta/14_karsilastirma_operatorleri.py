# Eşit mi? : ==
print(3 == 3)
print(3 == 5)

# Eşit değil mi? : !=
print(3 != 3)
print(3 != 5)

# Büyüktür : >
print(5 > 3)
print(3 > 5)

# Küçüktür : <
print(5 < 3)
print(3 < 5)

# Büyüktür Eşittir : >=
print("5 >= 5 :", 5 >= 5)
print("5 >= 10:", 5 >= 10)

# Küçüktür Eşittir : <=
print("5 <= 5 :", 5 <= 5)
print("5 <= 10:", 5 <= 10)

