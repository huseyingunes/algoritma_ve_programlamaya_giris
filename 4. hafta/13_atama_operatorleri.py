# Normal değer atama : =
a = 5 #a değişkenine 5 değerini atar
print(a)

# Çarparak değer atama : =
a *= 3 #a değişkenini 3 ile çarpar
print(a)


# Bölerek değer atama : =
a /= 3 #a değişkenini 3 e böler
print(a)


# Çarparak değer atama şu şekilde de yapılabilir
a = a * 3 # a *= 3 ifadesi ile aynı anlamı taşır
print(a)

# Bölerek değer atama şu şekilde de yapılabilir
a = a / 3 # a /= 3 ifadesi ile aynı anlamı taşır
print(a)
