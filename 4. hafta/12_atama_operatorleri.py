# Normal değer atama : =
a = 5 #a değişkenine 5 değerini atar
print(a)

# Toplayarak değer atama : =
a += 3 #a değişkenine 3 ekler
print(a)

# Çıkararak değer atama : =
a -= 3 #a değişkeninden 3 çıkartır
print(a)

# Toplayarak değer şu şekilde de yapılabilir
a = a + 3 # a += 3 ifadesi ile aynı anlamı taşır
print(a)

# Çıkararak değer şu şekilde de yapılabilir
a = a - 3 # a -= 3 ifadesi ile aynı anlamı taşır
print(a)
