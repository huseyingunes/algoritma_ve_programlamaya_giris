
liste = ["elma", "muz", "çilek", "vişne", "kivi"]
print(liste)

liste.remove("vişne")  # listenin istediğimiz elemanını listeden çıkartır
print(liste)

liste.pop(1)  # listenin istediğimiz sırasındaki elemanı listeden çıkartır
print(liste)

liste.pop()  # listedeki son elemanı listeden çıkartır
print(liste)

liste.clear()  # listedeki bütün elemanları siler
print(liste)
